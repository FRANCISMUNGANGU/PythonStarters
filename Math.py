# a module
# to include a module is import statement

import math
# built-in math functions
# min & max : return minimum and maximum values respectively inside a collection
x = min(100, 200, 400)
y = max(100, 200, 400)
print(x)
print(y)
# abs: converts to positive value
x = abs(-1.25)
print(x)
# pow: returns x to the power of y
x = pow(4, 3)
print(x)
# Math module
# sqrt
x = math.sqrt(64)
print(x)
# rounding off: round off upwards use ceil, downwards use floor
x = math.ceil(2.4)
y = math.floor(2.4)
print(x)
print(y)
# for pi use math.pi(returns 3.142.......)
